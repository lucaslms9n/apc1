#include <stdio.h>
 int main () {
   char tecla = 'a';
   printf("O valor de tecla = %c\n", tecla);
   tecla = 'f';
   printf("O valor de tecla = %c\n", tecla);
   
   int idade = 25;
   printf("O valor de idade é = %i\n", idade);

   float media = 5.5f;
   printf("O valor de media é = %f\n", media);

   double pi = 3.14156239;
   printf("O valor de pi é = %f\n", pi);
   
   return 0;
 }